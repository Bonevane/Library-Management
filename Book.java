public class Book {
    public int ID;
    public String title;
    public String author;
    public String genre;
    public int quantity;

    // Constructor for the Book Class
    public Book(int id, String t, String a, String g, int q){
        this.ID = id;
        this.title = t;
        this.author = a;
        this.genre = g;
        this.quantity = q;
    }
}
